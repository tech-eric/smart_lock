
#include "Delay.h"

/*********************************************************************************************************************

				用24为的定时器实现秒时间延时


**********************************************************************************************************************/


void Delay(uint32_t time){
	
			
	
			SysTick->LOAD=9*time;
			SysTick->CTRL=0x00000001;
			while(!(SysTick->CTRL&0x00010000));
			SysTick->CTRL=0x00000000;
	
}
